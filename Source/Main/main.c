int main()
{
	CardDeck_Init();
	if(CardDeck_Shuffle())
	{
		CardType_t cardVal;
		uint8_t i;
		while(CardDeck_GetNextCard(&cardVal))
		{
			printf("%d -\t%c %c\n", i, CardDeck_GetShape(cardVal), CardDeck_GetVal(cardVal));
			i++;
		}
	}
	else
	{
		printf("ShufflingError\n");
	}
	return 0;
}

