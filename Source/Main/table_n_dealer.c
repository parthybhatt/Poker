#include "player_bot.h"
#include "table_n_dealer.h"

/*******************************************************************************
* Constants and macros
********************************************************************************/
#define MAX_NUM_OF_BOTS		(5)

/*******************************************************************************
* Data types (Structs, enum)
********************************************************************************/
typedef struct
{
	PlayerBot_t bots[MAX_NUM_OF_BOTS];
	uint32_t    potAmt;
	uint8_t     roundNum;
} Table_t;

/*******************************************************************************
* Static Variables
********************************************************************************/
static Table_t Table;

/*******************************************************************************
* Static Functions Declaration
********************************************************************************/

/*******************************************************************************
* Public Functions
********************************************************************************/
void Table_Init()
{
	for(uint8_t i = 0; i < MAX_NUM_OF_BOTS; i++)
	{
		PlayerBot_Init(&Table.bots[i]);
	}
	Table.potAmt = 0;
	Table.roundNum = 0;
}

void Table_SetGame()
{
	//
}

void Table_GameRound()
{
	Table.roundNum++;
}

